# Polymorphism is a word that came from two greek words, poly means many and morphos means forms.
# if a variable, object or method perform different behavior according to situation, it is called polymorphism.
# Polymorphism  refers to methods/functions/operators with the same name that can be executed on many objects or classes.

# Topic of polymorphism:
#     1. Duck Typing
#     2. Operator Overloading
#     3. Method Overloading
#     4. Method Overriding

# Python function that can be used on different objects.
